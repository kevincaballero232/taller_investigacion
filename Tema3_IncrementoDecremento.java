public class Tema3_IncrementoDecremento {
    public static void main(String[] args) {
        System.out.println("=== TEMA 3: INCREMENTO Y DECREMENTO ===");
        
        int i = 5;
        int j = 5;

        i++; // Incrementa en 1 (i ahora es 6)
        j--; // Decrementa en 1 (j ahora es 4)

        System.out.println("Valor de i despues de i++: " + i);
        System.out.println("Valor de j despues de j--: " + j);
    }
}